package com.socialize.android.ioc.sample;

public class TestClassWithMultipleProperties {
	String string;
	int integer;
	long lng;
	short shrt;
	char chr;
	boolean bool;
	byte bte;
	float flt;
	double dbl;
	
	public TestClassWithMultipleProperties() {
		super();
	}

	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}

	public int getInteger() {
		return integer;
	}

	public void setInteger(int integer) {
		this.integer = integer;
	}

	public long getLng() {
		return lng;
	}

	public void setLng(long lng) {
		this.lng = lng;
	}

	public short getShrt() {
		return shrt;
	}

	public void setShrt(short shrt) {
		this.shrt = shrt;
	}

	public char getChr() {
		return chr;
	}

	public void setChr(char chr) {
		this.chr = chr;
	}

	public boolean isBool() {
		return bool;
	}

	public void setBool(boolean bool) {
		this.bool = bool;
	}

	public byte getBte() {
		return bte;
	}

	public void setBte(byte bte) {
		this.bte = bte;
	}

	public float getFlt() {
		return flt;
	}

	public void setFlt(float flt) {
		this.flt = flt;
	}

	public double getDbl() {
		return dbl;
	}

	public void setDbl(double dbl) {
		this.dbl = dbl;
	}
}